class Phone {
    constructor(name, company, operatingSystem, screen, price, battery, color){
this.name = name;
this.company = company;
this.operatingSystem = operatingSystem;
this.screen = screen;
this.price = price;
this.battery = battery;
this.color = color;
    }
info(){
    return `${this.company} ${this.name} ${this.color}`
}

credit_month_4(){
let credit_price = this.price * (4/100)
let months = 4;
let total_price = credit_price * months + this.price;
return `ЗАГАЛЬНА ЦІНА ПІСЛЯ 4 ВАЖКИХ МІСЯЦІВ КРЕДИТА - ${total_price}`
}
     
credit_month_6(){
    let credit_price = this.price * (5/100)
    let months = 6;
    let total_price = credit_price * months + this.price;
    return `ЗАГАЛЬНА ЦІНА ПІСЛЯ 6 ВАЖКИХ МІСЯЦІВ КРЕДИТА - ${total_price}`
    }

    credit_month_12(){
        let credit_price = this.price * (6/100)
        let months = 12;
        let total_price = credit_price * months + this.price;
        return `ЗАГАЛЬНА ЦІНА ПІСЛЯ 12 ВАЖКИХ МІСЯЦІВ КРЕДИТА - ${total_price}`
        }

}

let phone1 = new Phone("Iphone121", "Amerima", "ISOOS", 5,50000 ,2500 ,`black`)
console.log(phone1.credit_month_12())

